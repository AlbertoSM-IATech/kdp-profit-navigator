export const getPresetsForInteriorAndSize = (
  interior: string | null,
  size: string | null
): PaperbackPreset[] => {
  if (!interior || !size) return [];
  return paperbackPresets.filter(
    (p) => p.interior === interior && p.size === size
  );
};

export const getPresetById = (id: string): PaperbackPreset | undefined => {
  return paperbackPresets.find((p) => p.id === id);
};
